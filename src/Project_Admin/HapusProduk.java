    public void delData() throws SQLException{
        loadData();
        
        Koneksi kon = new Koneksi();
        kon.getData();

        int pesan = JOptionPane.showConfirmDialog(null, "Anda yakin menghapus data " + nambar + "?", "Konfirmasi",
                    JOptionPane.OK_CANCEL_OPTION);

        if (pesan == JOptionPane.OK_OPTION){
            try{
                Statement stat = (Statement) kon.getData().createStatement();
                String sql = "DELETE FROM barang where nama_barang = '" + nambar + "';";
                PreparedStatement p = (PreparedStatement) kon.getData().prepareStatement(sql);
                p.executeUpdate();
                kon.getData();
                Reset();
                JOptionPane.showMessageDialog(null, "Delete berhasil");
            }catch(SQLException er){
                JOptionPane.showMessageDialog(null, er.getMessage());
            }
        }
    }

